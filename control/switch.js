const Day = 4
switch (Day){
    case 1:
        console.log(1,"Mon")
        break;
    case 2:
        console.log(2,"Tue")
        break;
    case 3:
        console.log(3,"Wed")
        break;
    case 4:
        console.log(4,"Thu")
        break;
    case 5:
        console.log(5,"Fri")
        break;
    case 6:
        console.log(6,"Sat")
        break;
    case 7:
        console.log(7,"Sun")
        break;

    default:
        console.log('default case')
        break;
}